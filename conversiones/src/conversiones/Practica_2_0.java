    //metodo para mostrar los datos en la tabla
    void mostrarDatos(){
    String columnas[] = {"ID", "Valor", "Resultado", "Tipo"};
    DefaultTableModel modelo = new DefaultTableModel(null, columnas);

    tblDatos.setModel(modelo);
//codigo sql para mostrar toda la informacion de la tabla conversiones
    String sql = "SELECT * FROM conversiones";

    try {
        //llama a una conexion en otra clase parta poder insertar los valores en la base de datos
        conexion conector = new conexion();
        Connection con = conector.getConexion();
        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery(sql);

        while(rs.next()){
            String fila[] = new String[4];
            fila[0] = rs.getString("id_conversion");
            fila[1] = rs.getString("valor_ingresado");
            fila[2] = rs.getString("resultado");
            fila[3] = rs.getString("tipo_de_conversion");

            modelo.addRow(fila);
        }

        con.close();

    } catch(Exception e){
        //en caso de error muestra un mensaje de que fallo la conexion
        JOptionPane.showMessageDialog(this, "Error al mostrar: " + e.getMessage());
    }
}
    //metodo para guardar los datos en la base de datos
void guardarDatos(double valor, double resultado, String tipo){
    try {
        //llama a una conexion de otra clase
        conexion conector = new conexion();
        Connection con = conector.getConexion();
//codigo sql para poder insertar los datos en la tabla
        String sql = "INSERT INTO conversiones (valor_ingresado, resultado, tipo_de_conversion) VALUES (?,?,?)";
        PreparedStatement ps = con.prepareStatement(sql);

        ps.setDouble(1, valor);
        ps.setDouble(2, resultado);
        ps.setString(3, tipo);

        ps.executeUpdate();
        con.close();

    } catch(Exception e){
        //en caso de fallar la conexion muestra un mensaje de error
        JOptionPane.showMessageDialog(this, "Error al guardar: " + e.getMessage());
    }
}
    
