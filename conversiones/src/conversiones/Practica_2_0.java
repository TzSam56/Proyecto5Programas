    //Código para el boton de "Fahrenheit"
    private void btnFahrenheit1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFahrenheit1ActionPerformed
         try {
             //realiza las operaciones segun el boton seleccionado
        double v1, farh1;

        v1 = Double.parseDouble(txtValores.getText());
        farh1 = v1 * (9.0 / 5.0) + 32;

        lblResultado.setText(String.format("%.2f Fahrenheit", farh1));
        lblError.setText("");
//le da la informacion al campo de que tipo de conversion fue
        guardarDatos(v1, farh1, "C a F");

    } catch (NumberFormatException e) {
        //en caso de que el usuario escriba algun otro dato que no sea numerico salta este mensaje de error
        JOptionPane.showMessageDialog(this, "Solo se permiten valores numéricos");
    }    
    }//GEN-LAST:event_btnFahrenheit1ActionPerformed

    //Código para el boton de "Centigrados"
    private void btnCentigrados1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCentigrados1ActionPerformed
                                                    
    try {
        //realiza las operaciones segun los valores dados y el boton seleccionado
        double v1, cent1;

        v1 = Double.parseDouble(txtValores.getText());
        cent1 = (v1 - 32) * (5.0 / 9.0);

        lblResultado.setText(String.format("%.2f Centigrados", cent1));
        lblError.setText("");
//da la informacion para guardarla en la base de datos
        guardarDatos(v1, cent1, "F a C");

    } catch(NumberFormatException e){
        //en caso de que se escriba algun otro dato que no sea numerico salta un mensaje de error
        JOptionPane.showMessageDialog(this, "Solo se permiten valores numericos.");
    }

    }//GEN-LAST:event_btnCentigrados1ActionPerformed

