    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
      //realiza la conexion con la base de datos para poder mostrar los datos
        Connection conexion;
    String dbuser ="root";
    String dbpassw = "zeusonario";
    String databasename ="programas";
    String url ="jdbc:mysql://localhost:3306/" + databasename;

    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        conexion = DriverManager.getConnection(url, dbuser, dbpassw);
//codigo sql para que poder mostrar toda la informacion de la tabla empleados
        String sql = "SELECT * FROM empleados";
        PreparedStatement ps = conexion.prepareStatement(sql);
        java.sql.ResultSet rs = ps.executeQuery();

        // modelo de la tabla
        javax.swing.table.DefaultTableModel modelo = 
            (javax.swing.table.DefaultTableModel) tblDatos.getModel();

        // limpia tabla antes de cargar
        modelo.setRowCount(0);

        // Recorrer resultados
        while (rs.next()) {
            Object[] fila = new Object[7];
            //recopila los valores de cada campo y los muestra en la columna correspondiente
            fila[0] = rs.getInt("id_empleado"); 
            fila[1] = rs.getString("nombre_empleado");
            fila[2] = rs.getInt("horas");
            fila[3] = rs.getDouble("tarifa");
            fila[4] = rs.getDouble("basico");
            fila[5] = rs.getDouble("bonificacion");
            fila[6] = rs.getDouble("neto");

            modelo.addRow(fila);
        }

    } catch (Exception e) {
        //en caso de algun error con la conexion al mostrar salta un mensaje de error
        JOptionPane.showMessageDialog(this, "Error al mostrar datos");
        e.printStackTrace();
    }
    }//GEN-LAST:event_jButton3ActionPerformed

