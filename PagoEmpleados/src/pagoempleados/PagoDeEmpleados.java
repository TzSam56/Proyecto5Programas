    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
//Entrada
//realiza la conexion con la base de datos
try{
      Connection conexion;
            String dbuser ="root"; // Usuario MySQL
            String dbpassw = "zeusonario"; //Contraseña del mySQL
            String databasename ="programas"; //nombre de la db
            String url ="jdbc:mysql://localhost:3306/" + databasename;
            
            //Sentencia SQL con parametros
            String sql;
            sql = "INSERT INTO empleados(nombre_empleado, horas, tarifa, basico, bonificacion, neto)";
            sql += "VALUES (?,?,?,?,?,?)";
            
            // Lectura de datos del formulario
            String Empleado = txtEmpleado.getText();
            int horas = Integer.parseInt(txtHoras.getText());
            double tarifa = Double.parseDouble(txtTarifa.getText());
            
            //Calculos 
            double basico = horas * tarifa;
            double bonificacion = basico *0.2;
            double neto = (basico + bonificacion) * 0.9;
            
            String.format("%.3f", neto);
             String.format("%.3f", tarifa);
             
             //Mostrar resultados en el text area
            txtR.setText("Resumen de pago de Empleados\n");
            txtR.append("\nEl empleado: " + Empleado);
            txtR.append("\nEl basico es " + basico);
            txtR.append("\nLa bonificación es " + bonificacion);
            txtR.append("\nEl neto es: " + neto);
            
            //Insertar en la base de datos
            try {
                Class.forName("com.mysql.cj.jdbc.Driver"); //cargar el driver
                conexion = DriverManager.getConnection(url, dbuser, dbpassw);
                
                PreparedStatement pstm;
                pstm = (PreparedStatement) conexion.prepareStatement(sql);
                
                pstm.setString(1, Empleado);
                pstm.setInt(2, horas);
                pstm.setDouble(3, tarifa);
                pstm.setDouble(4, basico);
                pstm.setDouble(5, bonificacion);
                pstm.setDouble(6, neto);
                
                int rtdo = pstm.executeUpdate();
                
                if (rtdo == 1)
                    JOptionPane.showMessageDialog(this, "Fila correctamemnte insertada");
                else
                    throw new RuntimeException("No se pudo insertar la fila");
            }catch (ClassNotFoundException | RuntimeException | SQLException e){
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Conexion fallida");
                throw new RuntimeException(e);
            }
            
    }//GEN-LAST:event_jButton1ActionPerformed
    catch (Exception e){
        javax.swing.JOptionPane.showMessageDialog(this, "Ingresa datos validos (numeros)");
    }
    }
